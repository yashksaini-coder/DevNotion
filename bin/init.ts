#!/usr/bin/env tsx
/**
 * devnotion init — Interactive setup wizard.
 * Guides users through configuring all required environment variables,
 * validates each credential with a live API call, and writes .env.local.
 */

import inquirer from 'inquirer';
import { writeFileSync, existsSync, readFileSync } from 'node:fs';
import { join } from 'node:path';

const ENV_FILE = join(process.cwd(), '.env.local');

// ─── Color helpers ──────────────────────────────────────────────────────────
const bold = (s: string) => `\x1b[1m${s}\x1b[0m`;
const green = (s: string) => `\x1b[32m${s}\x1b[0m`;
const yellow = (s: string) => `\x1b[33m${s}\x1b[0m`;
const red = (s: string) => `\x1b[31m${s}\x1b[0m`;
const dim = (s: string) => `\x1b[2m${s}\x1b[0m`;

// ─── Validation helpers ──────────────────────────────────────────────────────
async function validateGitHub(token: string, username: string): Promise<string | true> {
  try {
    const res = await fetch(`https://api.github.com/users/${username}`, {
      headers: { Authorization: `Bearer ${token}`, 'User-Agent': 'DevNotion-Init' },
    });
    if (!res.ok) return `GitHub API error: ${res.status} ${res.statusText}`;
    return true;
  } catch (e) {
    return `Network error: ${e instanceof Error ? e.message : String(e)}`;
  }
}

async function validateGemini(key: string): Promise<string | true> {
  try {
    const res = await fetch(
      `https://generativelanguage.googleapis.com/v1beta/models?key=${key}`,
    );
    if (!res.ok) return `Gemini API error: ${res.status}`;
    return true;
  } catch (e) {
    return `Network error: ${e instanceof Error ? e.message : String(e)}`;
  }
}

async function validateOpenAI(key: string): Promise<string | true> {
  try {
    const res = await fetch('https://api.openai.com/v1/models?limit=1', {
      headers: { Authorization: `Bearer ${key}` },
    });
    if (!res.ok) return `OpenAI API error: ${res.status}`;
    return true;
  } catch (e) {
    return `Network error: ${e instanceof Error ? e.message : String(e)}`;
  }
}

async function validateAnthropic(key: string): Promise<string | true> {
  try {
    const res = await fetch('https://api.anthropic.com/v1/models', {
      headers: { 'x-api-key': key, 'anthropic-version': '2023-06-01' },
    });
    if (!res.ok) return `Anthropic API error: ${res.status}`;
    return true;
  } catch (e) {
    return `Network error: ${e instanceof Error ? e.message : String(e)}`;
  }
}

async function validateNotion(token: string, parentPageId: string): Promise<string | true> {
  try {
    const id = parentPageId.replace(/-/g, '');
    const res = await fetch(`https://api.notion.com/v1/pages/${id}`, {
      headers: {
        Authorization: `Bearer ${token}`,
        'Notion-Version': '2022-06-28',
      },
    });
    if (res.status === 404) return 'Page not found — check NOTION_PARENT_PAGE_ID';
    if (!res.ok) return `Notion API error: ${res.status}`;
    return true;
  } catch (e) {
    return `Network error: ${e instanceof Error ? e.message : String(e)}`;
  }
}

async function validateDevto(key: string): Promise<string | true> {
  try {
    const res = await fetch('https://dev.to/api/users/me', {
      headers: { 'api-key': key },
    });
    if (!res.ok) return `DEV.to API error: ${res.status}`;
    return true;
  } catch (e) {
    return `Network error: ${e instanceof Error ? e.message : String(e)}`;
  }
}

async function validateHashnode(token: string, publicationId: string): Promise<string | true> {
  try {
    const body = JSON.stringify({
      query: `{ publication(id: "${publicationId}") { title } }`,
    });
    const res = await fetch('https://gql.hashnode.com', {
      method: 'POST',
      headers: {
        Authorization: token,
        'Content-Type': 'application/json',
      },
      body,
    });
    if (!res.ok) return `Hashnode API error: ${res.status}`;
    const json = (await res.json()) as { data?: { publication?: { title?: string } } };
    if (!json.data?.publication) return 'Publication not found — check HASHNODE_PUBLICATION_ID';
    return true;
  } catch (e) {
    return `Network error: ${e instanceof Error ? e.message : String(e)}`;
  }
}

// ─── Writing helper ──────────────────────────────────────────────────────────
function writeEnvFile(vars: Record<string, string>): void {
  const lines = [
    '# DevNotion — generated by `devnotion init`',
    `# Generated: ${new Date().toISOString()}`,
    '',
    '# ─── GitHub ──────────────────────────────────────────────────────────────',
    `GITHUB_TOKEN=${vars.GITHUB_TOKEN ?? ''}`,
    `GITHUB_USERNAME=${vars.GITHUB_USERNAME ?? ''}`,
    '',
    '# ─── LLM Provider ───────────────────────────────────────────────────────',
    `LLM_PROVIDER=${vars.LLM_PROVIDER ?? 'gemini'}`,
    vars.GOOGLE_API_KEYS ? `GOOGLE_API_KEYS=${vars.GOOGLE_API_KEYS}` : '# GOOGLE_API_KEYS=',
    vars.OPENAI_API_KEY ? `OPENAI_API_KEY=${vars.OPENAI_API_KEY}` : '# OPENAI_API_KEY=',
    vars.ANTHROPIC_API_KEY ? `ANTHROPIC_API_KEY=${vars.ANTHROPIC_API_KEY}` : '# ANTHROPIC_API_KEY=',
    '',
    '# ─── Notion ──────────────────────────────────────────────────────────────',
    `NOTION_TOKEN=${vars.NOTION_TOKEN ?? ''}`,
    `NOTION_PARENT_PAGE_ID=${vars.NOTION_PARENT_PAGE_ID ?? ''}`,
    '',
    '# ─── Publishing ─────────────────────────────────────────────────────────',
    `PUBLISH_TARGETS=${vars.PUBLISH_TARGETS ?? 'notion'}`,
    `PUBLISH_MODE=${vars.PUBLISH_MODE ?? 'auto'}`,
    vars.DEVTO_API_KEY ? `DEVTO_API_KEY=${vars.DEVTO_API_KEY}` : '# DEVTO_API_KEY=',
    vars.HASHNODE_TOKEN ? `HASHNODE_TOKEN=${vars.HASHNODE_TOKEN}` : '# HASHNODE_TOKEN=',
    vars.HASHNODE_PUBLICATION_ID
      ? `HASHNODE_PUBLICATION_ID=${vars.HASHNODE_PUBLICATION_ID}`
      : '# HASHNODE_PUBLICATION_ID=',
    '',
    '# ─── Narration ──────────────────────────────────────────────────────────',
    `BLOG_TONE=${vars.BLOG_TONE ?? 'casual'}`,
    vars.FOCUS_AREAS ? `FOCUS_AREAS=${vars.FOCUS_AREAS}` : '# FOCUS_AREAS=',
    '',
    '# ─── Dashboard ──────────────────────────────────────────────────────────',
    `DASHBOARD_PORT=${vars.DASHBOARD_PORT ?? '3000'}`,
    vars.DASHBOARD_TOKEN ? `DASHBOARD_TOKEN=${vars.DASHBOARD_TOKEN}` : '# DASHBOARD_TOKEN=',
  ].join('\n');

  writeFileSync(ENV_FILE, lines + '\n', 'utf8');
}

// ─── Status printer ──────────────────────────────────────────────────────────
async function validateAndPrint(
  label: string,
  fn: () => Promise<string | true>,
): Promise<boolean> {
  process.stdout.write(`  Validating ${label}… `);
  const result = await fn();
  if (result === true) {
    console.log(green('✓'));
    return true;
  } else {
    console.log(red(`✗ ${result}`));
    return false;
  }
}

// ─── Main ────────────────────────────────────────────────────────────────────
async function main(): Promise<void> {
  console.log();
  console.log(bold('  DevNotion Setup Wizard'));
  console.log(dim('  Configures your .env.local for the 3-agent pipeline'));
  console.log();

  if (existsSync(ENV_FILE)) {
    const { overwrite } = await inquirer.prompt<{ overwrite: boolean }>([
      {
        type: 'confirm',
        name: 'overwrite',
        message: yellow('.env.local already exists. Overwrite?'),
        default: false,
      },
    ]);
    if (!overwrite) {
      console.log(dim('  Aborted — existing .env.local left untouched.'));
      process.exit(0);
    }
  }

  const vars: Record<string, string> = {};

  // ─── GitHub ───────────────────────────────────────────────────────────────
  console.log(bold('\n  1. GitHub'));
  const github = await inquirer.prompt<{ GITHUB_TOKEN: string; GITHUB_USERNAME: string }>([
    {
      type: 'password',
      name: 'GITHUB_TOKEN',
      message: 'GitHub Personal Access Token',
      mask: '●',
      validate: (v: string) => v.length > 10 || 'Token too short',
    },
    {
      type: 'input',
      name: 'GITHUB_USERNAME',
      message: 'GitHub username',
      validate: (v: string) => v.length > 0 || 'Required',
    },
  ]);
  Object.assign(vars, github);
  await validateAndPrint('GitHub', () => validateGitHub(vars.GITHUB_TOKEN!, vars.GITHUB_USERNAME!));

  // ─── LLM Provider ────────────────────────────────────────────────────────
  console.log(bold('\n  2. LLM Provider'));
  const { LLM_PROVIDER } = await inquirer.prompt<{ LLM_PROVIDER: string }>([
    {
      type: 'list',
      name: 'LLM_PROVIDER',
      message: 'Which LLM provider?',
      choices: [
        { name: 'Gemini (free tier available)', value: 'gemini' },
        { name: 'OpenAI (GPT-4o)', value: 'openai' },
        { name: 'Anthropic (Claude)', value: 'anthropic' },
      ],
    },
  ]);
  vars.LLM_PROVIDER = LLM_PROVIDER;

  if (LLM_PROVIDER === 'gemini') {
    const { GOOGLE_API_KEYS } = await inquirer.prompt<{ GOOGLE_API_KEYS: string }>([
      {
        type: 'password',
        name: 'GOOGLE_API_KEYS',
        message: 'Google AI API key (comma-separate multiple for round-robin)',
        mask: '●',
      },
    ]);
    vars.GOOGLE_API_KEYS = GOOGLE_API_KEYS;
    await validateAndPrint('Gemini', () => validateGemini(GOOGLE_API_KEYS.split(',')[0]!.trim()));
  } else if (LLM_PROVIDER === 'openai') {
    const { OPENAI_API_KEY } = await inquirer.prompt<{ OPENAI_API_KEY: string }>([
      { type: 'password', name: 'OPENAI_API_KEY', message: 'OpenAI API key', mask: '●' },
    ]);
    vars.OPENAI_API_KEY = OPENAI_API_KEY;
    await validateAndPrint('OpenAI', () => validateOpenAI(OPENAI_API_KEY));
  } else if (LLM_PROVIDER === 'anthropic') {
    const { ANTHROPIC_API_KEY } = await inquirer.prompt<{ ANTHROPIC_API_KEY: string }>([
      { type: 'password', name: 'ANTHROPIC_API_KEY', message: 'Anthropic API key', mask: '●' },
    ]);
    vars.ANTHROPIC_API_KEY = ANTHROPIC_API_KEY;
    await validateAndPrint('Anthropic', () => validateAnthropic(ANTHROPIC_API_KEY));
  }

  // ─── Notion ───────────────────────────────────────────────────────────────
  console.log(bold('\n  3. Notion'));
  console.log(
    dim(
      '  Create an Internal Integration at https://notion.so/my-integrations\n  then share your parent page with it.',
    ),
  );
  const notion = await inquirer.prompt<{
    NOTION_TOKEN: string;
    NOTION_PARENT_PAGE_ID: string;
  }>([
    {
      type: 'password',
      name: 'NOTION_TOKEN',
      message: 'Notion Integration Token (secret_…)',
      mask: '●',
    },
    {
      type: 'input',
      name: 'NOTION_PARENT_PAGE_ID',
      message: 'Parent page ID or URL',
      validate: (v: string) => v.length > 10 || 'Required',
    },
  ]);
  Object.assign(vars, notion);
  await validateAndPrint('Notion', () =>
    validateNotion(vars.NOTION_TOKEN!, vars.NOTION_PARENT_PAGE_ID!),
  );

  // ─── Publishing Platforms ─────────────────────────────────────────────────
  console.log(bold('\n  4. Publishing Platforms'));
  const { platforms } = await inquirer.prompt<{ platforms: string[] }>([
    {
      type: 'checkbox',
      name: 'platforms',
      message: 'Which platforms to publish to?',
      choices: [
        { name: 'Notion (always included)', value: 'notion', checked: true, disabled: true },
        { name: 'DEV.to', value: 'devto', checked: false },
        { name: 'Hashnode', value: 'hashnode', checked: false },
      ],
    },
  ]);
  const publishTargets = ['notion', ...platforms.filter((p) => p !== 'notion')];
  vars.PUBLISH_TARGETS = publishTargets.join(',');

  if (publishTargets.includes('devto')) {
    const { DEVTO_API_KEY } = await inquirer.prompt<{ DEVTO_API_KEY: string }>([
      {
        type: 'password',
        name: 'DEVTO_API_KEY',
        message: 'DEV.to API key',
        mask: '●',
      },
    ]);
    vars.DEVTO_API_KEY = DEVTO_API_KEY;
    await validateAndPrint('DEV.to', () => validateDevto(DEVTO_API_KEY));
  }

  if (publishTargets.includes('hashnode')) {
    const hashnode = await inquirer.prompt<{
      HASHNODE_TOKEN: string;
      HASHNODE_PUBLICATION_ID: string;
    }>([
      {
        type: 'password',
        name: 'HASHNODE_TOKEN',
        message: 'Hashnode Personal Access Token',
        mask: '●',
      },
      {
        type: 'input',
        name: 'HASHNODE_PUBLICATION_ID',
        message: 'Hashnode Publication ID (from your blog settings)',
      },
    ]);
    Object.assign(vars, hashnode);
    await validateAndPrint('Hashnode', () =>
      validateHashnode(vars.HASHNODE_TOKEN!, vars.HASHNODE_PUBLICATION_ID!),
    );
  }

  // ─── Publish mode ─────────────────────────────────────────────────────────
  const { PUBLISH_MODE } = await inquirer.prompt<{ PUBLISH_MODE: string }>([
    {
      type: 'list',
      name: 'PUBLISH_MODE',
      message: 'Default publish mode?',
      choices: [
        { name: 'auto — publish immediately', value: 'auto' },
        { name: 'draft — save as drafts for review', value: 'draft' },
      ],
    },
  ]);
  vars.PUBLISH_MODE = PUBLISH_MODE;

  // ─── Narration ───────────────────────────────────────────────────────────
  console.log(bold('\n  5. Narration'));
  const narration = await inquirer.prompt<{ BLOG_TONE: string; FOCUS_AREAS: string }>([
    {
      type: 'list',
      name: 'BLOG_TONE',
      message: 'Blog writing tone?',
      choices: [
        { name: 'casual — OSS dev energy (recommended)', value: 'casual' },
        { name: 'professional — confident, concise', value: 'professional' },
        { name: 'technical — architecture deep-dives', value: 'technical' },
        { name: 'storytelling — personal dev log', value: 'storytelling' },
      ],
    },
    {
      type: 'input',
      name: 'FOCUS_AREAS',
      message: 'Focus areas (optional, comma-separated)',
      default: '',
    },
  ]);
  vars.BLOG_TONE = narration.BLOG_TONE;
  if (narration.FOCUS_AREAS.trim()) vars.FOCUS_AREAS = narration.FOCUS_AREAS.trim();

  // ─── Dashboard ───────────────────────────────────────────────────────────
  console.log(bold('\n  6. Dashboard'));
  const dashboard = await inquirer.prompt<{ DASHBOARD_PORT: string; DASHBOARD_TOKEN: string }>([
    {
      type: 'input',
      name: 'DASHBOARD_PORT',
      message: 'Dashboard port',
      default: '3000',
      validate: (v: string) => !isNaN(Number(v)) || 'Must be a number',
    },
    {
      type: 'password',
      name: 'DASHBOARD_TOKEN',
      message: 'Dashboard access token (leave blank = no auth)',
      mask: '●',
    },
  ]);
  vars.DASHBOARD_PORT = dashboard.DASHBOARD_PORT;
  if (dashboard.DASHBOARD_TOKEN.trim()) vars.DASHBOARD_TOKEN = dashboard.DASHBOARD_TOKEN.trim();

  // ─── Write .env.local ─────────────────────────────────────────────────────
  writeEnvFile(vars);
  console.log();
  console.log(green(`  ✓ Written to ${ENV_FILE}`));
  console.log();
  console.log(bold('  Next steps:'));
  console.log(`  ${dim('$')} pnpm dev           ${dim('# run the pipeline for this week')}`);
  console.log(
    `  ${dim('$')} pnpm dashboard     ${dim('# start the web dashboard')} ${dim(`(http://localhost:${vars.DASHBOARD_PORT ?? 3000})`)}`,
  );
  console.log();
}

main().catch((err) => {
  console.error(red(`\n  Error: ${err instanceof Error ? err.message : String(err)}`));
  process.exit(1);
});
